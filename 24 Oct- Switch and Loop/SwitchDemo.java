class SwitchDemo{


	public static void main(String[] args){
	
	int value = 2;

	switch(value){

	case 1:
	System.out.println("1 is pressed");
	break;

	default:
	System.out.println("Wrong choice");
	break;
	
	case 4:
	System.out.println("2 is pressed");
	break;

	case 3: 
	System.out.println("3 is pressed");
	break;

	
	}

	}


}